package src.exercice.pOO.exercicio_15.entites;

public class Dados {
    private double altura;
    private char genero;

    public Dados(double altura, char genero) {
        this.altura = altura;
        this.genero = genero;
    }

    public double getAltura() {
        return altura;
    }


    public char getGenero() {
        return genero;
    }

}
