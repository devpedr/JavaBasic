package src.exercice.pOO.exercicio_13.entites;

public class Pessoa {
    private String name;
    private int years;

    public Pessoa(String name, int years) {
        this.name = name;
        this.years = years;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYears() {
        return years;
    }

    public void setYears(int years) {
        this.years = years;
    }
}



